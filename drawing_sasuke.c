void plot_pixel(int x, int y, short int line_color)
{
    if (x < 0) x = 0;
    else if (x > WIDTH) x = WIDTH;

    if (y < 0) y = 0;

    else if (y > HEIGHT) y = HEIGHT;

    *(short int*)(pixel_buffer_start + (y << 10) + (x << 1)) = line_color;
}



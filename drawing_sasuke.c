void cls() {

    int i = 0, j = 0;

    for (int k = 0; k < WIDTH * 2 * HEIGHT - 1; k += 2) {

        int red = ((data[k + 1] & 0xF8) >> 3) << 11;
        int green = (((data[k] & 0xE0) >> 5)) | ((data[k + 1] & 0x7) << 3);
        int blue = (data[k] & 0x1f);

        short int p = red | ((green << 5) | blue);

        plot_pixel(0 + i, j, p);

        i += 1;
        if (i == WIDTH) {
            i = 0;
            j += 1;
        }
        
    }
}

